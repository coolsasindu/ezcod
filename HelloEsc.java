class HelloEsc {
  
 public static void main(String args[]){
   
    System.out.println("Hello World This is\bezcod.blogspot.com "); // \b = backspace
    System.out.println("Hello World This is\f ezcod.blogspot.com "); // \f  = Form feed (*)
    System.out.println("Hello World This is\n ezcod.blogspot.com ");  // \n = Print New line
    System.out.println("Hello World\r This is ezcod.blog\tspot.com ");  // \r = Carriage return ( Enter) \t = tab (TAB Space)
    
 }
}